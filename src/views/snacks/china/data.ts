function unipFunc(arr) {
  const newArr = [];
  arr.forEach((item: never) => {
    if (arr.indexOf(item) !== arr.lastIndexOf(item) && newArr.indexOf(item) === -1) {
      newArr.push(item);
      return newArr;
    }
  });
  console.log('重复菜名菜名：', newArr);
}
