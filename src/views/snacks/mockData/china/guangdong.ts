潮式葱油饼
“潮式葱油饼”由于用炸的方法，且原料中加入富含淀粉的番薯，所以除了同样具有浓郁的葱香味外，还具有外酥香内松嫩的口感。
葱油饼是中国北方传统的面点小食，以面粉为主料，加入葱花，用平底锅煎至两面金黄，有浓郁的葱香味，是深受群众喜爱的人众化北方小食。“潮式葱油饼”吸取北方葱油饼的优点，结合潮州人的口味和潮州物产而进行改良。
炸制“潮式葱油饼”时，要注意翻转，以防受热不均匀。

