汕头老妈宫粽球
老妈宫粽球的馅料有咸、甜两种。咸馅主要是虾米、香菇、腊肠、（鱼帝）脯、莲子、栗子和南乳猪鬃头肉；甜馅主要是水晶馅、乌豆沙馅和绿豆沙馅。
老妈宫粽球用竹叶、咸水草扎成四角形，放进锅内煮约半小时，食用时解开竹叶，盛入盘中。
煮熟的老妈宫粽球外观棱角分明，晶莹的米粒清晰润滑，十分惹人喜爱。而其口味更是香糯诱人，甜馅清甘可口，咸馅则咸中透香，油滑而不腻，让人回味无穷。
老妈宫粽球是潮汕地区久负盛名的传统名小食。
