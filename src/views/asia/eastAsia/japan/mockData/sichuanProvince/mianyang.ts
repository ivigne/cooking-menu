锅魁中间有一个“龙眼睛”似的锅魁头，把龙眼牵出、提出来，整个锅魁一层层旋开而不会断，锅魁则层层都酥；混堂锅魁：其特色是泡，锅魁泡得老高；脆皮锅魁：以香味闻名，有各种馅，锅魁上有一个红印，油亮亮的；另还有芝麻锅魁、糯米锅魁、腌肉或卤肉夹锅魁以及各种味道与肉馅的锅魁，可达20多个品种。其独特风味与地域特色，将吸引众多本地和外地的食客。 [22] 

