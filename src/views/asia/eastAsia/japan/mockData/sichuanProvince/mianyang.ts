色绿质嫩，柔滑而富弹性，整洁而层次分明，食时兑料讲究，多配以麻辣酸调料，若浇以豆豉酱汁、窝油高醋，辣椒香油，大蒜芥末，视之绿盈水泛，食之麻辣冲，嚼之柔韧凉爽，寒热俱佳佳，清嫩爽口，阴阳相宜，鲜味独特。因只有用梓潼之水做出的片粉才有筋力，抖之不断，食之柔软，故成梓潼一绝。 [23] 

