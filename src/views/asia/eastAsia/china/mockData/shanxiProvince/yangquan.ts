粉条豆腐丝
阳泉市所属平定县有道名菜：粉条豆腐丝。这道菜主料是粉条和油豆腐丝，成菜特点是，豆腐丝是炸过的，外脆里嫩，菜看着很温和，没有一点热菜的感觉，但吃起来烫嘴，所以，地道的饭店都会说，我们做得
抿曲
