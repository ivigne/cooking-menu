过油肉
过油肉是中国传统菜肴，在山西、江苏、上海和浙江地区都有，从选料到制作上都与众不同。山西过油肉较为出名，具有浓厚的山西地方特色，号称“三晋一味”，其起源于明代晋东名城平定（今山西省阳泉平定县），各地做法不一。较著名的有大同、太原、阳泉、晋城过油肉，晋城的“大米过油肉”，特色是多汤水，搭配刚出锅的大米饭一起吃堪称一绝。2018年9月10日，“中国菜”正式发布，“山西过油肉“被评为山西十大经典名菜。2022年6月，被评为十大晋菜。
平定黄瓜干
平定黄瓜干，主要产于平定县后沟、河头两村，1368年－1398年（明洪武年间）开始生产。以清脆、爽口、香醇味厚、食用方便而受到人们的青睐。清朝被定为进贡皇室的物品，享有“龙筋”之誉。平定黄瓜
阳泉压饼
压饼是中共创建第一城，近现代女作家“民国四大才女”之一石评梅故乡的一种特色传统小吃。在阳泉地区广泛流行，称“漾泉”一绝，历史悠久，相传唐代平阳公主率军驻守娘子关时，就曾以此饼为行军干粮让

