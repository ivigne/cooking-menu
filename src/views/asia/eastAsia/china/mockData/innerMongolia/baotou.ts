小肥羊火锅
内蒙古小肥羊餐饮连锁有限公司是一家以自然人做发起人的股份制企业，公司于1999年8月诞生，以小肥羊特色火锅连锁为主业。
莜面
莜面是内蒙古地区“三件宝”之一。
稍美
（音shāomài，与南方烧麦不同）：该食品历史悠久。选用锡林郭勒草原鲜羊肉做馅，配以大葱，优质河套面粉做皮精制而成，上屉蒸熟即可。成品呈石榴状，可做宴席中的点心，又是民间美餐。德兴源稍美以皮薄、馅嫩、味香、形美著称，在稍美经营中独占鳌头。
手扒肉
手扒肉是红食中的一种。蒙古族人民称肉食为红食，蒙语叫“乌兰伊德”。顾名思义，“手扒肉”就是手抓羊肉，是蒙古族千百年来的传统食品，是牧民们的家常便饭。
铁锅焖面
是基于铁锅烩菜的基础上的一道类似农家饭一样的美食，是西北人民对美食研究制作出的精品。
羊杂碎
羊杂碎集羊内脏于一锅，形色繁多，肉质各异，味道酸辣兼宜，无腥不腻，汤鲜味美，入口生津，营养丰富，在气候偏冷的北方食用可御冷逐寒。
烤全羊
蒙古族传统名菜，为招待贵宾或举行重大庆典时的盛宴特制的佳肴。一般选用草原上膘肥、体重40斤左右的绵羊宰杀后，去毛带皮腹内加葱、姜、椒、盐等佐料整体烤制而成。烤完以后，羊形完整，羊跪在方木盘内，色泽金红，羊皮酥脆，羊肉嫩香。

