东坡饼
东坡饼又名空心饼、千层饼，是为纪念苏东坡而命名的，是湖北地区特色传统美食之一。关于“东坡饼”名称的由来，充满了人文色彩。相传苏东坡被贬海南儋县（现海南儋州）时，当地有一位卖环饼的年迈守寡老妪，她的手艺好，环饼质量高，可是因为店铺偏僻，不为人知生意一直不好。老妪得知苏东坡是著名文学家，就请他为店铺作诗。苏东坡怜悯她生活贫苦，环饼手艺又委实不错，就挥笔写下一首七绝：“纤手搓来玉色匀，碧油煎出嫩黄深。夜来春睡知轻重，压扁佳人缠臂金。”寥寥数句，勾画出环饼匀细、色艳、酥脆的特点和形似美人环钗的形象。老妪将此诗高悬门上，果然顾客盈门，生意兴隆，后来当地老百姓为纪念苏东坡，就名之以“东坡饼”，并在儋州流传今。“东坡饼”不但包含着奇特的饮食文化，还蕴藏着浓郁的乡情文化，深受人们的喜爱。
太和千张
鄂州市梁子湖太和千张，当地人习惯称“谢埠千张”，是鄂州著名的特产，以“薄、软、香”著称，是鄂东南地区的风味食品，堪称“湖北一绝”。太和千张薄如纸，色泽微黄，口感细腻，柔韧耐嚼，满口生香。太和千张可凉拌，可清炒，可煮食，久煮不变形不变色。当地民间有湖水煮湖鱼，佐以太和千张。梁子湖之鱼久负盛名，自古有武昌之鱼甲天下之说，以新鲜野鱼配天然湖水，杂以山泉、绿色无污染黄豆精制之太和千张，佐当地纯谷酒，可谓四绝。相传,在汉代淮南王刘安制作豆腐后不久,公元221年三国吴王孙权建都武昌（今鄂州）时，这里就开始制作千张皮，孙权迁都建业（今南京）后，委派大将军陆逊辅佐太子孙登留守武昌，太子经常到吴王山避暑和狩猎。吴王山下一小村庄，因地处两水交汇处，水运的方便，已成为“商贾骈集、货财辐辏，店铺鳞次、帆樯云聚”的商埠。时有一家谢氏豆腐作坊，每天从吴王山虎头泉处取水，做出的一种豆制品，它薄得几近透明，似一张张可捻开的宣纸，入口绵软、清香、甘甜，让人过齿难忘，这就是著名的谢埠千张（俗称太和千张）。到了宋代,此地商业日盛,油盐柴米布匹生意兴起,城乡豆腐店增多,豆浆、豆花和豆果等品种花样翻新,千张皮也就大宗制作起来；明清时列为朝廷贡品，光绪十一年（1885年）《武昌县志》将谢埠千张皮载入史书，距今传承已有1700多年的历史。

