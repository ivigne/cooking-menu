早堂面
早堂面，是沙市著名的传统早点，已有百年历史。关于早堂面的来历，比较令人信服的说法是在1895年沙市开埠后，当地一面馆老板根据这里的码头工人因从事体力劳动喜欢吃油水厚重食物的特点，制作了这种油厚码肥、汤鲜味美的面条。由于码头工人多在凌晨时分到面馆吃面后上工，故得此名。

酥炸春卷
