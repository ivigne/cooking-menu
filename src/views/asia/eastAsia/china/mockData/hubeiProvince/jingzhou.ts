荆州鱼糕
荆州鱼糕，湖北省荆州市特产，中国国家地理标志产品。荆州自古盛产鱼，鱼糕作为荆州的八大名肴其历史源远流长，相传为舜帝妃子女英所创，在荆楚一带广为流传，春秋战国时开始成为楚宫廷头道菜，直到清朝，仍是一道宫廷菜，据说乾隆尝过荆州花糕后脱口而咏：食鱼不见鱼，可人百合糕。现代鱼糕越做越为精美，人们用鱼和肥膘肉做糕，再用猪肉、山药等制成肉丸。肉丸垫底，鱼糕放在上面，最后以肚片、腰片烹炒盖帽，多菜共碗，俗称花糕、杂烩丸子，是荆州一带的红白喜事宴席上的头道菜。荆州鱼糕已经超越了单纯的饮食习惯而积淀成为一种独具特色的地方民俗文化。2013年01月23日，原国家质检总局批准对“荆州鱼糕”实施地理标志产品保护。2019年11月15日，入选中国特色农产品区域公用品牌。
鱼糕，作为荆州/沙市地区的八大名肴其历史源远流长。“荆州鱼糕”是用白色鱼肉制作的，可用红色鱼肉炸鱼丸子，和鸡肉、猪腰子、猪肚烩菜，成为荆州宴席名肴。
千张扣肉
此菜以肥瘦兼有的五花猪肉为原料，经煮炸，上红糖色，切成薄片，以碗扣住蒸熟，由于肉片薄如纸，形如梭，片数多，故名千张扣肉，又名梳子肉。
冬瓜鳖裙羹
冬瓜鳖裙羹，是以鳖（又称甲鱼、团鱼、脚鱼）的裙边和嫩冬瓜一起煨制的一种汤。其制法讲究，选料精细。先将裙边肥大的鳖宰杀后取下裙边切成块状，以旺火爆炒，再加鸡汤和调料煮，然后放入嫩冬瓜块清炖，直至鳖裙溶烂。
