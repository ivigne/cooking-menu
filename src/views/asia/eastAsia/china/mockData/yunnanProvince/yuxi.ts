新平腌菜十分讲究颜色的红亮，味道的酸甜，远近闻名；其中腌韭菜、韭菜根、芥辣菜（本地小油菜苗）算是几招绝活，本地扁杆青菜腌菜也格外惹人喜爱。多少年来，其中引人的奥秘难于说得清。
