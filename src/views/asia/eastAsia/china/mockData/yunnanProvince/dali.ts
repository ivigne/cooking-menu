剑川八大碗
剑川八大碗历史悠久，相传原来是大理国段白王来用招待周边南亚、东南亚等十六个国王所用的国宴，由于这些国家大都信奉佛教，所以是荤素搭配。由于段白王是剑川院傍人，后来传入剑川民间，成为待客的风味佳肴，相延至今，已近千年。八大碗荤素搭配，烹调序味美可口，老少皆宜。还将把白乡人民深深情意寄寓在菜肴之中，讲究意趣，体现了白族好客的情怀，富有民族特色。 [27]
剑川八大碗不但具有美好的寓意，而且各有自己隐喻吉祥与祝福的名字，分别是：红曲肉——“延年益寿”，粉蒸鱼——“年年有余”，千张肉——“千岁平安”，木耳豆腐——“和和睦睦”，酥肉垫竹笋——“舒舒服服”，水粉丝——“情意绵绵”，金丝坛豆——“相思百年”，百合扣肉——“百年好合”。
大理酸辣鱼
大理酸辣鱼以洱海鲫鱼为主材，配以红椒、青椒、木瓜、杨梅或乌梅为原料，用土灶烧柴火煮成，有时还会放入土豆和豆腐作为辅料，在大理民间，几乎家家会做酸辣鱼，其味美鲜香、酸辣可口，一直以来都是白族家宴上的主角以及待客的“招牌”。 [25]
乳扇
乳扇是以鲜牛奶为食材，混合食用酸浆使其凝固，然后拉抻如扇子形状的薄片，因而得名。云南十八怪之“牛奶做成扇子卖”，指的就是大理的乳扇。大理的牛奶还可以煎、烤、烫、烩、炸、煮、炒。 [25-26]
生皮
生皮是大理一带的白族人民传统的待客名菜，吃生皮的习俗相传始于南诏时期，凡是有白族人聚居的地方，一定有人吃生皮。所谓的生皮，就是在杀猪的时候，用稻草火烧去毛，故有“白族火烧猪”的美誉。生皮选材与制作都特别讲究，选取猪后腿肉和里脊、腰脊作为主料，用稻草或麦秸烧火将猪皮烤至表层发黄，然后用水洗净，以葱、姜、辣椒、炖梅等调料调制蘸水，生蘸而食。 [25-26]
喜洲破酥粑粑
喜洲粑粑是著名历史名城喜洲的一种特色小吃，主要原料为面粉。口味有甜、咸两种。制时皆用上下两层炭火，上层炭火为猛火，下层炭火为文火，在做好的面胚上刷上猪油之后入锅烘焙，在烤制过程中繁复刷几次油脂，烤香直至烤酥。喜洲粑粑外皮香酥而内在绵软，倍受人们喜爱，且层次分明，宛若苍山十九峰十八溪，实为美色可餐。
大理酸辣鱼
大理酸辣鱼，是云南大理传统美食之一。大理由于气候、环境、地理以及民族习俗等诸多原因形成了口味独特的饮食习惯，喜食酸、辣，酸能生津解暑，辣能祛湿开胃。而生活在洱海边的大理人对酸辣鱼的喜爱程度上不言而喻的。其特点：味美鲜香，酸辣可口，百尝不厌。
雕梅扣肉
