麻衣馓子
陆良的麻衣馓子是用麦面作原料，和好面后扭成“千钧扣”油炸的糖果。陆良“麻衣馓子”已有200年的历史，是地方传统风味食品，也是馈赠亲友的佳品。 [54]
陆良板鸭
陆良腊鸭，即板鸭，是选用壮而肥的仔鸭宰杀后，用适量食盐腌制而成的，每只重2公斤左右，造型为杏仁形，挂于室外清洁、干净、阳光充足和干燥通风的烤鸭架上，风吹日晒，数日后晒干即成板鸭。 [54]
沾益辣子鸡
沾益辣子鸡是中国餐饮特产，是滇菜代表作。 [18]
韭菜花
韭菜花制作时，将半籽半花的韭菜花剁细，加入盐巴、白酒，搅拌均匀，放入罐内，用半年的时间使韭菜花肉质糖化，然后拌上干苤蓝丝、辣椒、红糖、白酒腌制，待呈黄红色即可。
蒸饵丝
饵块（饵丝）是云南传统美食，“蒸饵丝”则是曲靖市独创，在明代形成了完整的技法和理念，之后不断丰富完善。
其他
此外，曲靖市还有花山带皮羊肉、洋芋鸡、会泽稀豆粉、烧饵块……等美食名吃。
酸菜红豆汤
酸菜红豆汤源自曲靖市师宗县，此汤特点是豆子软而香，酸菜酸而脆，汤味酸辣可口，食之令人胃口大开，回味悠长。做法是选用昭通上好的红豆，淘洗干净，置于锅中煮，煮开后换小火慢慢熬，熬至豆软汤稠。熬好红豆汤后，把腌好的酸菜切碎后用昭通酱、干辣子、蒜苗、姜片等炒香，然后倒入熬好的红豆汤，放入胡椒面，煮上几分钟，即可食用。
会泽稀豆粉
会泽稀豆粉是云南曲靖市会泽县著名的小吃稀豆粉，制作原料主要有稀豆粉、米线等。
陆良板鸭
曲靖市陆良县是云南省第一大坝子（平原之意），有“鱼米之乡”的美誉。陆良大坝子的南盘江蜿蜒似带，缓缓流过。陆良水资源丰富，江河沟渠纵横，密如珠网一般，加之这里四季如春，是养鸭难得的好地方。
曲靖韭菜花
“韭菜花”是曲靖颇有名气的传统名物食品。据传，“韭菜花”的生产起源于清末，迄今已有一百余年的历史了。韭菜花是用新鲜韭菜花与苤蓝丝、辣椒混合在一起，经腌制而成的。因韭菜花味突出，故取名为韭菜
麻衣馓子
麻衣馓子是以小麦面、白砂糖为原料而制成的的油炸类糕点，其状如“千钧扣”，色泽金黄，表层为芝麻糖衣，内里灌满糖饴，味道甜润而柔腻。陆良麻衣馓子以其“香、脆、酥、甜”的特点而闻名，距今已有
