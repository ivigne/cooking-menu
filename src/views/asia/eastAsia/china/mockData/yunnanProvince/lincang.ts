佤族酸肉
佤族酸肉是沧源佤族自治县小吃。将牛肉用淘米水泡洗，再用清水洗净，切片，入缸，加花椒叶、盐、米饭拌匀，装入陶罐，压实，上盖。用草木灰与泥巴和泥，密封罐口，腌渍1个月以上即成酸肉。在储存期间随时可以取食，但取后仍需盖严罐口，取出的部分需在三、五天内吃完，否则容易变质。食用时大多切成薄片，与青蒜同炒。
耿马毫崩
耿马毫崩是云南省临沧市耿马县的特色小吃。“毫崩”就是油炸米花糖。云南省临沧市耿马县和孟定镇，泼水节的“毫崩”（油炸米花糖）是家家必备的节日食品。
“烤崩”的具体做法是：将糯米用冷水浸泡，淘洗干净后用蒸笼蒸熟，做成饼状，晾晒到三成干，然后放入红糖水中浸湿，又把它晒干，这样重复两三次后，将浸过糖水的干糯米饼放在香油锅里炸脆，即成成品。
火腿木瓜鸡
火腿木瓜鸡主要原料是腊火腿、白花木瓜、山地子鸡。此属药膳佳肴。
制作方法：把鸡斩成小块，用水煮去掉血水，木瓜去皮和核，切丝，姜切丝；火腿切片。锅放少许油先把鸡肉和姜先炒一下，放入木瓜丝、火腿和盐一起炒出香味，加水煮到鸡肉酥烂可口，加胡椒粉即可起锅。
稀豆粉米线
稀豆粉米线是临沧市特色风味小吃之一。制作以豌豆粉、米线为原料，配以佐料而成。将豌豆粉调成浆过滤后煮成稀粥炖好备用，在烫好的米线上盖以稀豆粉，拌上芝麻、生姜、油辣椒、花椒油、蒜泥、青蒜叶、味精、酱油等，食之味香可口。
鸡肉烂饭
鸡肉烂饭，佤族语称“布安纳亚”，是佤族菜谱中最著名的一道菜，也是佤族待客的上等佳肴，因其比稀粥要稠一些，有经过加工的鸡肉混合，故俗称鸡肉烂饭。
其做法是把刚开口叫的公鸡用竹签戳死后放在火上把毛烧光，拔去毛根，掏去内脏，切块，放入冷水里煮。水沸后放入大米、五茄皮、酸笋、姜、芝麻、辣椒、花椒、木浆子、香芭草、盐、苏子等佐料，边煮边搅，米煮成稀粥便成鸡肉烂饭。佤族鸡肉烂饭选用本地土鸡，忌讳用白毛鸡煮制。拉祜族也把鸡肉烂饭视为上等佳肴。 [27]
