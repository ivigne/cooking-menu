咸菜
咸菜是朝鲜族喜爱的佐餐食品，多以桔梗，蕨菜，白菜，萝卜，黄瓜，芹菜等为原料，洗净后切成片，块，丝，用盐卤上，然后再拌以芝麻，蒜泥，姜丝，辣椒面、香菜面、味精、糖稀、虾酱等多种调味品，轻微发酵。
辣白菜
辣白菜是一种腌菜，是中国朝鲜族的传统美食，也是日常饮食中不可缺少的一道菜。
打糕
朝鲜族传统食品之一。打糕的历史比较长，早在18世纪朝鲜族的有关文献中已有记载，当时称打糕为“引绝饼”，为传统食品之一。
松饼
松饼形状如饺子。是先将和好的加有土豆淀粉的米面擀成小面片，再把拌有芝麻、枣、核桃、松子仁、糖的小豆馅包在里面，放到铺满松树叶的笼屉上蒸熟而成。
冷面
延边朝鲜族喜吃冷面。冷面的主料为白面，荞麦面和淀粉。
狗肉
狗肉是朝鲜族喜欢吃的肉食。一般都是大块地吃，过去多在三伏天吃，认为热天吃了能滋祉身体。
延边烤牛肉
将新鲜牛肉去筋膜、脂油，切成片，加冰醋、朝鲜族酱油、蒜末、洋葱泥、西红柿泥、白糖、胡椒粉、味精、香油拌匀，腌制四个小时，另备海鲜酱酒、辣椒末、白糖、白醋、香菜末，大蒜等调味品分装于小碗、炭火炉放在桌子中央，上放铁箅子，用筷子夹肉片放在箅子上烧烤，蘸调料后吃。
五谷饭
朝鲜族吃五谷饭由来已久。每逢正月十五，农民用江米、大黄米、小米、高粱米、小豆分别堆放到闷锅里做成五谷饭吃。
米酒
米酒是朝鲜族爱喝的一种稻米发酵饮料。米酒是招待客人的佳品，如有客来访，主人总要端上碗自家酿制的米酒。这种酒比黄酒的色稍白一点，而且还略带甜味。
