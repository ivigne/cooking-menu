庆岭活鱼
庆岭活鱼，吉菜美食之一，庆岭地区所做的把蒿活鱼。庆岭不过是吉林省吉林市所辖蛟河市一个山谷小村的名字，由于紧邻松花湖、庆岭森林公园、红叶谷等风景区，建有颇具规模的庆岭活鱼美食街，因而远近闻名，如今“庆岭活鱼”游到了鱼米之乡江浙沪，游到了广东、北京，招摇于美味佳肴之林。
