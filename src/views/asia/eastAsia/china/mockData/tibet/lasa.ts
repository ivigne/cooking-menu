酥油茶
酥油茶和糌粑是拉萨藏餐的主要代表。酥油茶是用酥油、砖茶和食盐混合在一起做成的，口感十分独特，据说可以缓解高原反应。
糌粑
糌粑是将青稞炒面与酥油茶采用一定比例混合在一起，吃的时候先在碗里搅拌，边吃边捏，方法独特。拉萨很多藏餐厅能品尝到酥油茶和糌粑。
藏面
藏面是最常见的藏族日常饮食，是将面条煮熟后放入牛肉汤和少量牛肉，口感和内地的面条没什么大区别。一般的甜茶馆、路边小店都有藏面。很多甜茶馆都有藏面，面条多为挂面，口感也基本差不多。
甘孜黄凉粉
甘孜黄凉粉是拉萨最受欢迎的小吃之一。就是凉粉切片，用酱油、醋、蒜汁、辣椒拌起来食用。
青稞酒
青稞酒藏语叫做“羌”，顾名思义，青稞酒就是用青稞制成的。是藏民最喜欢喝的酒。酒的口感略有些发，但清冽甘爽，度数不高，后劲不小。
