灯笼海参
灯笼海参选用大连灯笼海参本地特产刺参为主料，配以虾仁、鱼蓉、黄瓜等辅料蒸烧而成。成菜海参呈黑褐色，围配粉红色的“宫灯”，冷暖色调和谐，造型美观。灯笼口味成鲜，质感糯软，海参鲜嫩成香，给人以滋味的享受和欣赏的美感。此菜营养丰富，具有补肾和治疗水肿等药用价值。
