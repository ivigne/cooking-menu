豆花泡馍
豆花泡馍的原料由大豆、锅盔馍和各种调料三大部分组成，其制作方法是先将大豆挑捡洗净泡软，和水后用打浆机磨成豆浆，然后入锅煮熟，放入消泡剂搅匀消泡，再将石膏用水泡开，与豆浆同时倒入保温桶内，迅速加盖，过七八分钟后，就结成了凝固而洁白如玉的豆花。 [24]
西府扯面
扯面是陕西和山西的传统面食，已有3000年历史。以宝鸡的西府扯面最为正宗，为中华名吃。主要原料有上白面粉、鸡蛋、菠菜、红萝卜、调味品、臊（sào）子等。扯面主要流行于陕西、山西以及甘肃的徽县、两当、成县。 [26]
岐山臊子面
臊子面又名“蛟汤面”，典出定居于岐山的周文王挥剑斩蛟龙的故事。岐山臊子面以“薄、筋、光、煎、稀、汪、酸、辣、香”而著名，面条细长，厚薄均匀，臊子鲜香，红油浮面，汤味酸辣，筋韧爽口，老幼皆宜。宋朝大文学家苏东坡曾写诗称赞：“剩欲去为汤饼客，却愁错写弄獐书。” [25]
搅团（水围城）
用面搅成的浆糊，在水滚时，一手握一棍子搅动，另一手均匀撒各样杂面，否则杂面会结块，不能和水分充分结合，以至于结块部分久煮不熟，既影响视觉，又影响味觉，也造成不必要的浪费。棍子顺时针搅几下，逆时针搅几下，熟后配以调汁。 [27]
宝鸡擀面皮
擀面皮是陕西关中地区特有的传统名小吃，尤以西府宝鸡地区为最佳，宝鸡岐山擀面皮2011年被认定为中华名小吃。擀面皮是从唐代冷淘面演变而来。具有筋斗、柔软、凉香、酸辣可口的特点，是关中西府小吃代表之一，也是陕西最具代表的小吃之一。岐山人的擀面皮做法很非凡，别处的米面皮和酿皮是把面调成糊状，放在特制的铁笼上蒸。而岐山擀面皮则是先擀成面，然后再蒸，蒸熟后再切成比凉皮稍宽的条状，吃法和米面皮基本一样，不同于米面皮的是，擀面皮口感较硬，韧度高，有筋性。 [28]
陇县马蹄酥
陇县马蹄酥又名蜜馅儿是陕西省陇县传统风味小吃，圆形似马蹄而得名，是陇县民间走亲访友的名贵糕点。马蹄酥为江阴传统特色糕点。清朝末年仿制镇江同名糕点，并经改进成面。该产品采用上等面粉、赤豆为原料，加果仁为辅料，用糯米发酵后制成。因成品四周厚、中间薄、形似马蹄而得其名。其特点为酥、松、软兼备，色、香、味俱佳。
岐山醋粉
岐山醋粉是陕西省宝鸡市岐山地区独特的特色小吃，吃起来滑嫩细糯、醋香扑鼻，又能助消化，特别是夏季吃起来清凉爽口，又可清热解暑。
