岐山面
岐山面是陕西关中地区的一种传统特色面食，有其悠久的历史。岐山面以薄、筋、光、煎、稀、汪、酸、辣、香而著名。岐山面面条细长，厚薄均匀，臊子鲜香，红油浮面，汤味酸辣，筋韧爽口，老幼皆宜。岐山面在关中地区有其非常重要的地位，在婚丧、逢年过节、孩子满月、老人过寿、迎接亲朋等重要场合都离不开。
凤县浆水面
凤县浆水面，其味酸、辣、清香，别具一格，相传该名是由汉高祖刘邦与臣相萧何在此吃面时所起。它一直是凤县人民喜爱的面食，浆水菜的菜以芹菜为佳，白菜、萝卜缨也可。
臊子面
臊（sào）子面是中国北方的特色传统面食之一、是著名小吃，以宝鸡的岐山臊子面和临汾的汾城臊子面最为正宗。在山西晋南地区、陕西关中平原及甘肃陇东等地方流行。臊子面历史悠久，其中含有配菜比如豆腐，鸡蛋等，做法简单。臊子就是肉丁的意思。对于山西人来说，臊子面的配色尤为重要，黄色的鸡蛋皮、黑色的木耳、红色的胡萝卜、绿色的蒜苗、白色的豆腐等材料，既好看又好吃。
凤翔腊驴腿
凤翔腊驴腿是凤翔区久负声名的传统的特色名菜，陕西肉食品传统的名特产，此膳食据传说，清末时期有位姓苏名石娃的，当时制作的腊驴腿以其特色远近闻名，“腊驴腿”生产技术加工工艺就是他传于后世的。
