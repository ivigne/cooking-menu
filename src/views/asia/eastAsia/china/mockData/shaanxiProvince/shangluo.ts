搅团：本是当地百姓贫苦时的日常食品，但因其原料为杂粮，天然健康，加之做法更加精细，逐渐成为了风味小吃。搅团所用面粉必须是粗粮，多为玉米面搅团的吃法很多，浇热汤酸菜、做凉水“鱼鱼”凉调、炒食、烩搅团块等等皆可。商洛人吃搅团，大多要调辣子，调得碗里红如血方才够味。
