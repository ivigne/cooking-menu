宜春扎粉
宜春扎粉是江西省著名的小吃。以色白如银，条索柔韧，入口爽滑著称。手工扎粉生产可上溯至明朝，可谓历史悠久。说起扎粉，许多去过宜春的人都吃过，南方富庶的鱼米之乡，自古以来就有将富余的米做成米粉的传统。
樟树清汤
樟树清汤（樟树传统称之为“猴子”包面，是一种地方特色的馄饨）有着数百年的历史，从明末清初形成，是一道久享盛名的风味小吃。
罗城扎粉
罗城扎粉是江西万载地方特产，以纯大米为原料，以百年传统工艺精制而成，以其香纯和韧性而闻名，其不含任何防腐剂和添加剂，是纯天然绿色食品。
樟树炒粉
你要是来过一次樟树，一定要吃次正宗的樟树炒粉
老表土鸡汤
老表土鸡汤，是江西省宜春市袁州区传统的特色名菜，属于赣菜系袁州菜一支。因其肉质鲜嫩，鲜香爽口兼具滋补养身的神奇功效而被列入“十大赣菜”之一。老表土鸡汤的主料土鸡为袁州当地特产袁州土鸡。相传此菜于朱元璋有关。
包圆
包圆是江西省宜春市铜鼓县特有的名点，属于客家菜。是招待贵宾的佳肴；年夜饭必备食品。其做法与饺子相似，但馅心很讲究，由腊肉、冬笋、香菇、鸡肉、莲子及各种调味品组成。外皮是由芋子去皮加红薯粉
