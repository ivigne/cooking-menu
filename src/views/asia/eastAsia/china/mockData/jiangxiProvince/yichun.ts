炒八扎
炒八扎又叫八扎，宜丰特有的名菜。具有汤汁油润，质地香醇，清爽可口，食味鲜浓等特点。用猪精肉，猪肝、猪腰、猪肚子、猪心，猪脑花等制成。
红薯丸子
红薯丸子是用红薯制作的小吃。红薯含有丰富的淀粉、膳食纤维、胡萝卜素、维生素A、B、C、E以及钾、铁、铜、硒、钙等10余种微量元素和亚油酸等。
