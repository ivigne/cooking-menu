烧蒲菜
烧蒲菜是淮阳地区独有的传统名菜，属于豫菜系。被列为河南省地方名菜之一。它以龙湖特产蒲子下端的嫩芯为原料，加配佐料烧制而成。烧蒲菜可单独烧制，也可与肉、鱼、蛋拼合烧制。种类有虾仁烧蒲菜、蛋黄烧蒲菜、鱼片烧蒲菜等。不论哪种烧蒲菜，均色白如玉，鲜嫩清香，别具风味。 [18]
邓城叶氏猪蹄
叶氏猪蹄以清滑爽口，柔韧脱骨，味醇酥嫩，香气浓郁出名，独具风味，吃过后，唇齿留香、回味无穷猪蹄本身含有丰富的胶原蛋白和多种维量元素，益气补肾，强身健体。
叶氏猪蹄在清洗、焖煮时，按配方和诀窍掌握调味料、水温、火候，并随季节而异。其制作方法大致如下：首先是将新鲜猪蹄剔除蹄甲，去浮毛和圬垢，放入清水中漂洗干净，而后将猪蹄放入陈年老汤中，文火焖煮两三个小时。汤中按配方加入陈皮、肉桂、豆莞、白芷、丁香、草果、砂仁、良姜、茴香、花椒等几十种作料。煮熟的猪蹄要及时捞出，用油布擦去上面的血污和油汤，即可出售了。邓城叶氏猪蹄凉热食用均佳。 [19]
太康肘子
太康肘子是享誉中原的风味名吃，用料为上等猪肘，辅以特制名贵材料和独特制作工序，特点“肥而不腻、色泽鲜亮、浓香怡人、酥烂可口。”被誉为“天下美味之大成”而驰名中原。
孔集卤鸡
孔集卤鸡是河南鹿邑传统的特色名吃，属于豫菜系。此菜滑酥肉烂，烂而不胎，肥而不腻，色鲜味美，醇浓厚，温中益血、补精添髓、促进发育、延缓衰老而闻名于世。一百多年前由太康县麦糠屯姓何名豆的传入孔集。首继人孔昭灼，传孔宪体，孔宪来；孔宪来传孔庆连、孔庆贵。
固墙热豆腐
商水县固墙热豆腐。在点制过程中不用石膏，全用清浆，豆腐精细、鲜嫩、松软，浇上特制的辣椒汁（主要原料有辣椒、香油、精盐、味精、香椿叶、焦花生、焦芝麻、生姜等），美味可口。
逍遥镇胡辣汤
逍遥镇胡辣汤是中华风味名吃之一，色香味俱佳，且能醒酒提神，开胃健脾，源于河南省周口市西华县逍遥镇。正宗的胡辣汤中只用胡椒不用辣椒，即“胡乱辣”，各种辣味加在一起。经过民间若干年的加工发展，一种以适合北方人口味、辣味醇郁、汤香扑鼻的胡辣汤在逍遥镇诞生。
