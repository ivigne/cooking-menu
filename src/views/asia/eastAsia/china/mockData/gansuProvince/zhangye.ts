牛肉小饭
牛肉小饭，是甘肃省张掖最有名的物色美食早餐，入口牛肉嫩滑爽口，面丁疙瘩爽滑无比，口感特别好。
西北大菜
西北大菜又称“香饭”，是甘肃省张掖市筵席的主菜。民间俗有“唱戏凭的腔，筵席看香饭”的说法。筵席的好差，从香饭上看分晓。
粉皮面筋
粉皮面筋是一种传统面食。在张掖农村，农民秋收后，家家户户，要晒粉皮面筋迎亲接友，逢年过节或是喜庆之时，粉皮面筋是不可缺少的临时性招待食品。产妇坐月子时也由娘家赠送一篮粉皮面筋。现在农村晒粉皮面筋之风也还在流行。
搓鱼面
搓鱼面又称搓鱼子，是甘肃省张掖市特有的传统面食小吃。因其成品的形状中间粗、两头尖，酷似小鱼而得名，搓面鱼虽做法稍显复杂，但其口感甚佳，可口宜人，吃法上可干吃，也可炒菜吃。

糊馎
糊馎是民乐县地方小吃之一，特点是清香可口。
油泡泡
油泡泡，河西各地均有制做，而山丹的与众不同，因为里面搀了煮熟的黄参。
臊面
