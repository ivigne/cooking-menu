炒拨拉
炒拨拉是山丹县最有名的一种地方小吃，到山丹的游客一定都吃过这种有地方特色的小吃，炒的时候烟熏火燎，吃的时候再加上啤酒那是一个享受。那叫一个美。
灰碱面
灰碱面是张掖农村待客的上食，也是判断姑娘和媳妇做饭手艺的一个标志。
鱼儿粉
鱼儿粉是甘肃省有名的传统小吃。用精细的大豆粉制作，形状象小鱼，颜色有白有红，配上调料、小菜，活像一条条小鱼在碗里。
羊肉垫卷子
羊肉垫卷子是以羊肉、面卷、调味料为主要食材制作而成的属于甘肃河西走廊地区的地方特色小吃，发源于祁连山、焉支山一带，最早起源于甘肃省永昌县的羊羔肉垫面卷。
牛肉小饭
牛肉小饭，是甘肃省张掖最有名的物色美食早餐，入口牛肉嫩滑爽口，面丁疙瘩爽滑无比，口感特别好。
西北大菜
西北大菜又称“香饭”，是甘肃省张掖市筵席的主菜。民间俗有“唱戏凭的腔，筵席看香饭”的说法。筵席的好差，从香饭上看分晓。
粉皮面筋
粉皮面筋是一种传统面食。在张掖农村，农民秋收后，家家户户，要晒粉皮面筋迎亲接友，逢年过节或是喜庆之时，粉皮面筋是不可缺少的临时性招待食品。产妇坐月子时也由娘家赠送一篮粉皮面筋。现在农村晒粉皮面筋之风也还在流行。
搓鱼面
搓鱼面又称搓鱼子，是甘肃省张掖市特有的传统面食小吃。因其成品的形状中间粗、两头尖，酷似小鱼而得名，搓面鱼虽做法稍显复杂，但其口感甚佳，可口宜人，吃法上可干吃，也可炒菜吃。

糊馎
糊馎是民乐县地方小吃之一，特点是清香可口。
油泡泡
油泡泡，河西各地均有制做，而山丹的与众不同，因为里面搀了煮熟的黄参。
臊面
臊面是甘肃张掖有名的传统小吃。与外地的臊子面无论是从口味还是做法上都不同。甘肃张掖市民的传统早餐，深受当地民众喜爱，味道醇厚辣香，吃完回味无穷，多与韭菜馅包子搭配，各家餐馆都有各自从不外
