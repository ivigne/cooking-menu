河州包子是临夏特产，面皮好、馅子考究，有包子、合子、角馍之分。以牛羊肉和韭菜或胡萝卜或白萝卜混合剁碎搅匀为馅，装笼蒸熟，盛入盘中，淋以椒油，看上去晶莹剔透。 [16]
河州面片
河州面片，按制作工艺可分清汤面片、酸汤面片、河沿面片、炒面片等。先将面粉用水调和、揉搓、捏团，捏成粗条状，之后掐成小团，蘸油搓成小条，稍发片刻，压扁之后在沸水中揪入面片。待熟后用笊打入碗中，放入清汤或臊子，撒上蒜苗丝、香菜即可食用。
