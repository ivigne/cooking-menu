三北藕丝糖
三北藕丝糖是产自浙江慈溪的传统名点。相传，清代雍正年间，慈溪沈师桥有位叫沈永年的糕点师傅，做得一手好糕点，并不断创新，制作出了一种外形为柱状，粗细如同人的中指、长不过三寸似的糕点又似糖点的食品。折断来看，在这细细的的柱状里，意整整齐齐排列着数十个细状小孔，活似被折断的藕，后人就把这种食品称之为藕丝糖，因地处三北，所以也叫三北藕丝糖。
浙麦冬
浙麦冬是百合科沿阶草属草本植物的根。浙麦冬呈纺锤形,似枣核。表面黄白色或淡黄色,有纵纹。质柔软断面黄白色,半透明。具木质芯。气微香，肉质具油性糖质。喝之发粘。《余姚六仓志》载麦冬，来自川,近盛产塘沙地，医家言胜于川产，故称姚冬。浙麦冬分布在中国慈溪市西部和北部平原以及余姚市东部和北部平原地区。浙麦冬喜湿润而较耐干旱，喜冬暖夏凉而有较强耐寒力；土壤由浅海沉积物发育而成，为淡涂泥土属夜阴地或半夜阴地。质地为沙质壤土，通气性好,水、肥、气协调，保水保肥力强，是浙麦冬最佳适生环境。浙麦冬繁殖方式为根分株繁殖。浙麦冬具有养阴生津，润肺清心的功效。用于肺燥干咳，虚捞咳嗽，津伤口渴，心烦失眠，内热消渴，肠燥便秘，明白喉功效。
