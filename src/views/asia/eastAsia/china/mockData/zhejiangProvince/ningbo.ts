宁波岱衢族大黄鱼
宁波岱衢族大黄鱼，浙江省宁波市特产，全国农产品地理标志。宁波地域位于中纬度地带，属亚热带季风气候区。南部有向中亚热带过度的痕迹；东部具有海洋性气候性特征。风向随季节交替明显，多异常天气。夏冬季长，春秋季短，四季分明。冬无严冬，夏无酷暑，温暖润湿，光照、热量、雨水资源丰富，适宜养殖大黄鱼。宁波岱衢族岱衢族大黄鱼具有体型细长、头型浑圆、吻短而细巧、体色金黄、线条优美等特征；体被圆鳞，体背面及上侧面黄褐色，侧面及下侧面金黄色，背鳍及尾鳍均灰黄色，胸鳍和腹鳍黄色。2013年4月15日，原中华人民共和国农业部正式批准对“宁波岱衢族大黄鱼”实施农产品地理标志登记保护。
朱金木雕
