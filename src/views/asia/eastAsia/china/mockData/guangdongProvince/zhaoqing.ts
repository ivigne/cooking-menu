鼎湖上素
鼎湖上素，发源于广东省肇庆市鼎湖区，为广东特色素菜，属于粤菜。“上素”是高级菜之意。该菜是由广东肇庆鼎湖山庆云寺一位老和尚创于明朝永历年间，以银耳为制作主料，其烹饪技巧以蒸菜为主，食时鲜嫩滑爽，清香四溢，乃素菜上品。
肇庆裹蒸
肇庆裹蒸，广东省肇庆市特产，中国国家地理标志产品。肇庆裹蒸和其它地方的粽子不同，一般的粽子是用竹叶包的，呈长条状或者三角形，而肇庆裹蒸粽是用当地的冬叶包，里面放猪肉、绿豆、五花肉或者腊肠、咸蛋黄，用具有特殊香味儿的冬叶裹在一起，放进装着开水的大缸里，用柴火进行煲煮。一般需要煮上五六个小时，里面的五花肉、绿豆和蛋黄的香味儿才能很好地和融入糯米里。随着饮食文化的进步，肇庆裹蒸品种越来越丰富，有瑶柱、冬菇、虾仁等，但是不管怎么变化，都少不了绿豆“作伴”，原因是肇庆本地气候较为湿热，绿豆能很好地排解人体的热毒，起到强身健体、养生美容的功效。2012年07月18日，原国家质检总局批准对“肇庆裹蒸”实施地理标志产品保护。保护范围为肇庆市端州区、鼎湖区、高要区3个区现辖行政区域。
茶油鸡
茶油鸡是四会市最著名的一道菜，用珍贵清香的野山茶油烹饪而成。野山茶油是中国特有的珍稀食用油，具有特殊的油香。它产量极少，是茶油中的珍品。此种山茶油富含不饱和脂肪酸，不含胆固醇，其营养价值甚至超过橄榄油，更具有生津通络、活血解毒的功效。茶油沸点低，用茶油烹饪的茶油鸡具有鲜香、嫩滑、无腥味的特点。茶油鸡是四会人招待客人的必备佳肴。
