汕头牛肉丸
汕头牛肉丸又称手捶牛肉丸，是广东省潮汕地区最著名的传统小食之一，起源于潮菜，在汕头已有近百年历史，它以柔、润、鲜、爽而驰名。其制作技艺源自2000多年的官廷菜肴“捣珍”，这种制作技艺在潮汕地区流传也有近百年历史。手捶牛肉丸是潮汕地区最为普遍及大众化的民间小食，它既可作为点心小食，又可作为一道汤菜上筵席。潮汕人用两根特制的铁棒，每根3斤重，呈方形或三角形，用双手轮流捶打，把牛肉打成肉浆。并且用牛肉、牛骨熬汤来煮牛肉丸，从而使牛肉丸更具有浓郁的牛肉味，同时还增加用沙茶酱作酱碟吃，被评为潮汕名小食。2019年1月8日，汕头牛肉丸被汕头人民政府列入汕头市第六批非物质文化遗产。
