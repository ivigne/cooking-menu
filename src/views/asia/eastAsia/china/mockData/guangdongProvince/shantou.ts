生炒鱼面
生炒鱼面是一道色香味俱全的传统名点，属于粤菜系。是广东汕头的特色美食。将鱼肉用刀刮下剁成鱼肉茸，去净筋，盛在盆内，加入味精、精盐、使力揉和，搓成团。薯粉用布包扎，然后将鱼肉压扁，扑上薯粉，放在按板上，用木棍碾成大薄片，放进开水锅里泡一下捞起，用清水漂净晾干水分后，用刀切成丝，拉开成面条样。
酥皮蟹盒
酥皮蟹盒是广东汕头一带的传统名点，属于粤菜系。色泽金黄，馅香皮脆。把猪肉、蟹肉、虾肉、香菇、荸荠肉均切细粒，用淀粉水拌过，下鼎炒香，加入精盐、味精、胡椒粉、芝麻油和湿淀粉（少许）调成馅料。取面粉160克，加入猪油80克拌和，搓成酥心，又把面粉240克，加入猪油80克和清水90克拌成面皮。将酥心、面皮各分成24件。用面皮包上酥心，用棍子擀薄摊开，卷起再擀，再卷起，用刀一切为二，逐件擀成薄圆形。先取1张皮子，放入馅料，再取1张皮子覆盖在上面，四周捏紧，再卷成花纹边，制毕后，下六成热油鼎，用文火炸至金黄色取出，沥去油便成。
