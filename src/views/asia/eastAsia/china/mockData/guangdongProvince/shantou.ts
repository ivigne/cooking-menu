东里猪脚饭
东里猪脚饭是广东汕头澄海区传统的小吃，属于潮菜系。入口软烂无渣、肥而不腻、香气四溢、胶绵而不沾牙。选料方面，猪脚的卤制过程是在陈年老卤汤的基础上，酌量加入上好的酱油、冰糖、八角、豆寇、丁香、香菇、大蒜头等原料，猛火煮开后放入猪脚，半个小时后改为小火熬煮，约3个多小时整个猪脚香汁渗透、皮肉软烂，即可熄火。待卤汤凉却后捞去上层凝结之猪油，然后装入沙锅，一般每一个沙锅装4只猪脚，再用保鲜膜覆盖沙锅口后置于冰柜冷藏结冻即成。
