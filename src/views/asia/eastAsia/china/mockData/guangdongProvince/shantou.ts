潮汕蚝烙
蚝烙是广东潮汕地区的传统小吃，“蚝烙”是潮州久负盛名的传统民间小食，属于粤菜系。鲜美酥香，酥而不硬，脆而不软。采用优质雪粉，甚至连猪油都要用本地猪的鬃头肉煎出来的猪油，制作的每一步骤十分考究。现身《舌尖上的中国》的潮汕“蚝烙”掀起汕头市新的“蚝烙热”。已有数百年历史的潮汕美食“蚝烙”再度受到海内外食客热捧。
