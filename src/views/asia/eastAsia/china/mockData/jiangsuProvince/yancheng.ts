建湖藕粉圆子
以藕粉为原料，用白糖、枣泥、芝麻粉拌以荤素油脂及桂花为馅丸，在开水中多次氽制而成。形如弹丸，呈淡紫色。
白炖鲻鱼
鲻鱼是盐城名特海产，肉肥味美，配以猪油丁、火腿片、香菇片、笋片等，上笼以旺火蒸熟。炖熟的鲻鱼肉呈蒜瓣状，汤清味浓，色泽美观，鲜嫩可口。烩素鱼皮。相传为清初盐城兜率寺厨师发明。
烩素鱼皮
相传为清初盐城兜率寺厨师发明。
奇园蟹黄包
以个大膘肥黄多的活母蟹和精面为原料制作，味道极鲜美，尤以奇园菜馆的蟹黄包为佳，故名。
首乌糕
用滨海县特产何首乌粉制成，味道鲜美，色质透明，可养生，可治白发，已成宴席佳品。
四鳃鲈鱼
产于响水县的灌河。嘴大、鳞细、体侧扁。菜花时节鱼体有黑白相间的花纹，又称“菜花鲈鱼”。鱼头部有重叠的鳃纹，看似四鳃。肉质细嫩，鱼汤味鲜，可与鸡汁媲美，旧时曾为贡品。盐城糖麻花。
阜宁大糕
糕片白如雪，柔如云，上口香甜、滋润。主要以糯米粉精制而成。
大纵湖醉蟹
大纵湖蟹个大黄多。洗净后，配以盐城曲酒、米酒。
葛武嫩姜片
特点是鲜甜脆嫩、形如薄纸、美味可口。其制法是精选寒露前三天采收的鲜嫩生姜，经过13道工序制作而成。
东台鱼汤面
为江苏省东台市名点，原系宫廷食品，其汤浓稠如乳，点滴成珠，面白细匀，鲜而不腻，为传统名点。1924年曾获巴拿马博览会金奖。
生炝条虾
东台市特产。将生虾先加盐、曲酒杀菌去腥后，再加入腐乳汁、白酱油、白糖等辅料，即可食用。此菜尤以清明前食用最佳。
东台西瓜
东台西瓜曾以甘甜汁多、消暑解渴、风味独特而成为县府的礼品，人们夏日常食的果品。
弶港醉螺
江苏省东台市弶港镇特产，又名糖泥螺，始于清乾隆年间，其特点是：香、甜、脆、嫩。
叉烤野鸡片
江苏盐城的一道传统名菜。盐城东临黄海，西襟湖荡，盛产野鸡。入冬时雄雉肉多味美，素为时令名菜。尤以叉烤野鸡片，外脆里嫩，油润爽口，更增添野生禽的特有风味。野鸡，又名山鸡，雄者羽毛美丽，体状且大，雌者体形较小，制菜雄者为佳。野鸡肉的钙、磷、铁含量较一般高很多，并且富含蛋白质、氨基酸，对贫血患者、体质虚弱的人是很好的食疗补品。野鸡肉还有健脾养胃、增进食欲、止泻的功效。野鸡肉有祛痰补脑的特殊作用，能治咳痰和预防老年痴呆症，是野味中的名贵之品。
盐城草炉饼
草炉饼，就是黄烧饼，但是，它的制作方法与一般的烧饼不同。草炉饼的炉子，是用大口的砂缸，垫空横搁，将砂缸的底敲掉，作为炉门，将缸口凝合在墙壁上，周围用泥沙固定。缸内的四周，全是贴饼的地方了
