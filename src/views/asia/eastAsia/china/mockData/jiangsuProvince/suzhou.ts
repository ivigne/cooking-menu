太湖梅鲚
梅鲚，又名湖鲚，俗称毛叶鱼、刀鲚，宋代苏东坡称为“银刀”，又名湖鲚其他地区也有称为凤尾鱼或烤子鱼。鲚鱼，体侧扁，腹稍润、尾细长，银白色；体形略扁薄，头尖小而口大，两眼着生于头的前上端，腹部稍阔，尾则细狭，鳞细色白，整体呈窄长的毛竹叶状。
太湖白虾
太湖白虾，亦称脊尾白虾。素有“太湖白虾甲天下”之说。太湖白虾形状与其它淡水虾别无二致，白虾色白壳薄，通体透明。《湖州府志》载：白虾，出太湖，色白壳软，而味鲜。白虾营养价值高，含有丰富的蛋白质、维生素和多种微量元素。民间美食者喜用白虾制成醉虾，别有一番风味。
采芝斋糖果
苏式糖果享誉中华，有明货、炒货、软糖、特味4大类150多个品种，如松子糖、粽子糖、花生糖、三色松子软糖、脆松糖、松子南枣糖等，其中以采芝斋独家生产的粽子糖最为有名。
松鼠桂鱼
此鱼色泽酱红，外脆内嫩，甜酸适口，乾隆食后大为赞赏，松鼠鱼便传扬出名。如今，松鼠鱼以桂鱼作原料。桂鱼肉质细嫩，骨疏刺少，经剞花、油炸后、头大口张，尾部翘起，内似翻毛，形似松鼠，浇上虾仁、笋干、番茄酱卤时还会发出嗤嗤如松鼠的叫声。
阳澄湖大闸蟹
阳澄湖大闸蟹，又名金爪蟹。产于阳澄湖。蟹身不沾泥，俗称清水大蟹，体大膘肥，青壳白肚，金爪黄毛。2011年9月起，阳澄湖大闸蟹带有防伪标志。章太炎夫人汤国梨有“不知阳澄湖蟹好，今生何必在苏州”。
叫化鸡
叫化鸡是江苏常熟名菜，又称黄泥煨鸡。相传，很早以前，有一个叫化子，沿途讨饭流落到常熟县的一个村庄。一日，他偶然得来一只鸡，欲宰杀煮食，可既无炊具，又没调料。他来到虞山脚下，将鸡杀死后去掉内脏，带毛涂上黄泥、柴草，把涂好的鸡置火中煨烤，待泥干鸡熟，剥去泥壳，鸡毛也随泥壳脱去，露出了鸡肉。约100多年以前，常熟县城西北虞山胜地的“山景园”菜馆根据这个传说，去粗取精，精工效法创制此鸡。
地方小吃
苏州小吃是中国四大小吃之一（南京夫子庙小吃、上海城隍庙小吃、苏州玄妙观小吃和湖南长沙火宫殿小吃并称为中国四大小吃）。“松鹤楼”是老字号的苏式餐馆；“老苏州茶酒楼”以传统苏帮菜而闻名；“朱鸿兴面馆”和“绿扬馄饨”等物美价廉，比较适合大众消费。遍布苏州的面店，表明浇头品种很多、讲究汤水的苏式面条是广受欢迎的小吃。 [25]
美食街：太监弄、十全街、学士街、李公堤、凤凰街等，碧凤坊、李公堤为中国餐饮文化名街。
苏式招牌菜：松鼠桂鱼、响油鳝糊、蟹粉蹄筋、清溜虾仁、氽糟、母油整鸡、太湖莼菜汤、雪花蟹斗、樱桃肉、酱汁肉、薰鱼、（鱼巴）肺汤、三件子、蜜汁火方、暖锅、枣泥拉糕等。
苏式糖果：轻糖松子、粽子糖、浇切片、三色松子软糖、脆松糖、松子南枣糖等。
苏式蜜饯：苏州制作蜜饯的历史可上溯到三国时代，清代是苏式蜜饯的鼎盛时期，其中以“张祥丰”最为著名，历来是“宫廷食品”。苏式蜜饯现有160多个品种，以金丝蜜枣、奶油话梅、金丝金桔、白糖杨梅、九制陈皮最为著名。
